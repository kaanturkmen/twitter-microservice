package kafka;

public class TwitterKeys {
    String consumerKey = "";
    String consumerSecret = "";
    String token = "";
    String secret = "";

    public TwitterKeys() {
    }

    public String getConsumerKey() {
        return consumerKey;
    }

    public String getConsumerSecret() {
        return consumerSecret;
    }

    public String getToken() {
        return token;
    }

    public String getSecret() {
        return secret;
    }
}
